				<td colspan="2"><span class="postbody">I don't know if this is in the wrong place......
<br />

<br />
Ok, first, welcome to Linux. To do this, I prefer you use GNOME, because I don't know if it works in KDE. Or Xfce. Don't even ask about Fluxbox.
<br />

<br />
You can usually get Wine (the program, silly) at this website: <a href="http://winehq.com/site/download" target="_blank" class="postlink">http://winehq.com/site/download</a>.
<br />

<br />
Since I am assuming you are using Ubuntu (6.06 Dapper Drake), all screenshots will be of Ubuntu. If you are <span style="font-weight: bold">*not*</span> using Ubuntu, and it doesn't work, sorry, I can't help ya there. If it does, just tell me what distro worked.
<br />

<br />
First, you're probably frustrated right now. LiveSwif doesn't work out-of-the-box with a fresh Wine installation. Some of you probably created seperate Windows partitions just for LiveSwif (I was too stupid too do that). Others of you thought that Wine would work. If you have a seperate Windows Partition, KEEP IT. The Linux version I've figured out is buggy at it's best times.
<br />

<br />
[url=http://imageshack.us]
<br />
<img src="../../../img471.imageshack.us/img471/6770/openwith6tm.png" border="0" />
<br />
[/url]
<br />

<br />
Since you are a new Linux user, you probably aren't too fond of the terminal, but I want you to select "Use a Custom Command", and type:
<br />

<br />
</span><table width="90%" cellspacing="1" cellpadding="3" border="0" align="center"><tr> 	  <td><span class="genmed"><b>Code:</b></span></td>	</tr>	<tr>	  <td class="code">wine</td>	</tr></table><span class="postbody">
<br />

<br />
Install LiveSwif. This is easy. VERY EASY.
<br />

<br />
Press Ctrl+H to make hidden files appear.
<br />

<br />
Navigate to .wine
<br />

<br />
Go to "drive_c"
<br />

<br />
Then, You should be in a place that says "Windows" and "Program Files". You should know what to do from here, pretend it's your old C:\ drive.
<br />
Click "Open" and see what happens. If LiveSwif appears and no bug box appears, congratsulations! Skip the rest!
<br />

<br />
<img src="../../../img269.imageshack.us/img269/4095/error6dc.png" border="0" />
<br />

<br />
Usually, a box will appear telling you you're "Microsoft Comm Control" is too old. Just click okay, and LiveSwif SHOULD appear.
<br />

<br />
It usually does, with a problem. It tells you that comctl32.dll is MISSING. That's because Wine doesn't come with it.
<br />

<br />
Download the file <a href="http://www.dll-files.com/dllindex/dll-files.shtml?comctl32" target="_blank" class="postlink">here.</a>
<br />

<br />
Okay, now you have the file, on the desktop (or other directory) but am probably more confused. This is where it would be helpful if you were using Gnome, because I do things that are in Nautilus (If you don't know what Nautilus is, at the top of the screen go to <span style="font-weight: bold">Places ---&gt; Home Folder</span> <img src="images/smiles/icon_biggrin.gif" alt="Very Happy" border="0" /> That's right, Nautilus is you're File Manager).
<br />

<br />
If you didn't already, do to you're home folder.
<br />

<br />
Make the hidden files appear again.
<br />

<br />
Navigate to <span style="font-weight: bold">.wine</span> .
<br />

<br />
Go to folder <span style="font-weight: bold">drive_c</span>.
<br />

<br />
Then, <span style="font-weight: bold">Windows</span>.
<br />

<br />
Finally, <span style="font-weight: bold">system32</span>
<br />

<br />
Place "COMCTL32.dll" in System32. Close Nautilus. 
<br />

<br />
Re-open LiveSwif. Now, don't think it's bug-free. Let me tell you some known issues.
<br />

<br />
<img src="../../../img396.imageshack.us/img396/6774/taskbar1on.png" border="0" />
<br />

<br />
While this isn't so bad, you usually will get a box instead of a LiveSwif symbol.
<br />

<br />
<img src="../../../img181.imageshack.us/img181/1374/playingpretend4yv.png" border="0" />
<br />

<br />
LMV files like to pretend that they are .gz files. You can't double-click them to open LiveSwif anymore. Instead, open LiveSwif and open the file.
<br />

<br />
Even if you have the file "flashplugin-nonfree" installed, it will always say:
<br />

<br />
</span><table width="90%" cellspacing="1" cellpadding="3" border="0" align="center"><tr> 	  <td><span class="genmed"><b>Code:</b></span></td>	</tr>	<tr>	  <td class="code">
<br />
Failed to create flash player window, it seems that you did not install Flash player yet.&nbsp; please visit http&#58;//www.macromedia.com/software/flashplayer/&nbsp; to install it.
<br />
</td>	</tr></table><span class="postbody">
<br />

<br />
Sorry! No testing before you export to SWF.
<br />

<br />
Don't play with the window, like making it smaller, and bigger, smaller, and bigger, this takes a lot of RAM, and sometimes makes a random crash.
<br />

<br />
Random crashing is the last biggest problem. Make sure to save frequently!
<br />

<br />
Let's hope Linux support is a planned feature for 3.0. If not, AT LEAST test it in Wine first.....
<br />

<br />
Thank you for reading,
<br />
