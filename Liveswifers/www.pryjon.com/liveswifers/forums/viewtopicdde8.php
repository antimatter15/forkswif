				<td colspan="2"><span class="postbody">First suggestion: get a book on Macromedia Flash.  Although Flash is very different from LiveSwif, you will learn alot of the theory and practice you need to know before getting started.
<br />
Second suggestion: take a look at the sample game that came with your copy of LiveSwif, and examine all the code.  It takes quite a bit of work to get any sort of gravity and there is not even any rebounding built in.  You may be able to find some good information on a Flash coders forum, and possibly even a game engine, but I would not expect students to be able to code their own physics library without prior experience in programming.  You should probably give them most of the code, but go over how it works.  There is not much chance though that they will be able to code a game in about 20 lines of code.
<br />

<br />
Which brings up a question, how much experience do your students have?  Working with LiveSwif can benefit almost every skill level, but in order to make any suggestions, I think we need to know if these students have had a programming class before (not necessarily ActionScript/JavaScript).
<br />

<br />
My tutorial for the DeadSwif FPS (first person shooter) is a fairly simple game whose coding the students should be able to comprehend by the end of the course (see <a href="http://tobenjaminjones.googlepages.com/" target="_blank" class="postlink">http://tobenjaminjones.googlepages.com</a>), and Blackninja (<a href="http://www.freewebs.com/gcompany3/liveswif_works2.html" target="_blank" class="postlink">http://www.freewebs.com/gcompany3/liveswif_works2.html</a>) has some nice simple (but clever!!) items that could be used earlier in the course.
<br />

<br />
If they have never programmed before, they will need to know about variables, variable types (number, string of characters, object, maybe even arrays), loops and functions.
<br />
As far as animations go, they will need to be taught the concept of sprites (called movie clips in any Flash guidebooks you may read), lines/paths/beziers (not necessarily an in depth understanding, but just what they look like by using the "curve" tool), gradients, color codes and transparency.
<br />

<br />
When the the course start?
<br />

<br />
For the beginning of the course, you should probably teach the drawing part and the coding part separately.  Learning to code often takes a long time to sink in, and in the meanwhile, they will not be able to do anything with the code so they might as well learn to draw.  I have often had classes that are part lecture and part lab where the different things are done on different days (University classes here in the States are usually 2-3 days a week and so we would have lecture on one of the two days and lab on the other).  I think this approach might serve you well (have coding on one day and graphics on another).
<br />
Then, part of the way through the course, you should have them combine the elements.
<br />
I think a good "course project" you may want to consider is building a racing game.  This would require some sprites for cars, some ActionScript for keyboard events determining the winner.  It is also extensible, so students that really are getting the hang of programming and drawing can make some spectacular improvements.
<br />

<br />
Are you going to tell the student about this forum?  They may be able to get some assistance here, but we might not know how much to help (and how much would be considered cheating).
<br />

<br />
I guess perhaps I should ask you, how much experience do you have with programming?  (Or with Flash?)  If you have a lot of experience with this stuff, then I apologize for making these suggestions which you have probably already considered, and I hope you don't feel I am insulting you.  If you don't have much experience, let me congratulate you on this huge undertaking, and wish you the best of luck!  Either way, I am willing to try to be of any assistance you may need.
<br />
